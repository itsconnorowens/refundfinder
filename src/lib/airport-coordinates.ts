// Helper functions for airport operations
export function searchAirports(query: string): Airport[] {
  if (!query || query.length < 1) return [];

  const lowerQuery = query.toLowerCase();
  return airports
    .filter(
      (airport) =>
        airport.code.toLowerCase().includes(lowerQuery) ||
        airport.icao.toLowerCase().includes(lowerQuery) ||
        airport.name.toLowerCase().includes(lowerQuery) ||
        airport.city.toLowerCase().includes(lowerQuery) ||
        airport.country.toLowerCase().includes(lowerQuery)
    )
    .slice(0, 10); // Limit to 10 results
}

export function getAirportByCode(code: string): Airport | undefined {
  return airports.find(
    (airport) => airport.code.toLowerCase() === code.toLowerCase()
  );
}

export function getAirportByICAO(icao: string): Airport | undefined {
  return airports.find(
    (airport) => airport.icao.toLowerCase() === icao.toLowerCase()
  );
}

export function validateAirportCode(code: string): boolean {
  return airports.some(
    (airport) => airport.code.toLowerCase() === code.toLowerCase()
  );
}

export function validateAirportICAO(icao: string): boolean {
  return airports.some(
    (airport) => airport.icao.toLowerCase() === icao.toLowerCase()
  );
}

export function getAirportsByRegion(region: string): Airport[] {
  return airports.filter(
    (airport) => airport.region.toLowerCase() === region.toLowerCase()
  );
}

export function getAirportsByCountry(country: string): Airport[] {
  return airports.filter(
    (airport) => airport.country.toLowerCase() === country.toLowerCase()
  );
}

export function getAirportsByHubType(hubType: Airport['hubType']): Airport[] {
  return airports.filter((airport) => airport.hubType === hubType);
}

export function getActiveAirports(): Airport[] {
  return airports.filter((airport) => airport.isActive);
}

export function getAirportCoordinates(
  code: string
): { latitude: number; longitude: number } | null {
  const airport = getAirportByCode(code);
  if (!airport) return null;

  return {
    latitude: airport.latitude,
    longitude: airport.longitude,
  };
}

export function validateAirportExists(code: string): boolean {
  return validateAirportCode(code);
}

